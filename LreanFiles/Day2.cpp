//数据类型
#include <iostream>

void main() {

	char c = 'A';	//定义一个字符变量c，并赋值为字符A
	std::string str = "ABCD";	//定义一个字符串变量str，并赋值为ABCD
	short sint = 10;	//定义一个短整型变量sint，并赋值为10
	int i = 100;	//定义一个整型变量i，并赋值为100
	long lint = 1000;	//定义一个长整型变量lint，并赋值为1000
	long long llint = 10000;	//定义一个长长整型变量llint，并赋值为10000
	float f = 3.14f;	//定义一个单精度浮点型变量f，并赋值为3.14
	double d = 3.1415926;	//定义一个双精度浮点型变量d，并赋值为3.1415926

}